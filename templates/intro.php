<div id="introFeatures">
    <p>This is a free and open source and secure information system for general practice, using the <a href="https://icpc-3.info" target="_blank">ICPC-3</a> for coding.</p>
    <h3>Features</h3>
    <ul>
        <li>Free software, licensed under ISC License</li>
        <li>Self-hosted on your own webspace</li>
        <li>Patient data is encrypted</li>
        <li>Supports:
        <ul>
            <li>multiple databases</li>
            <li>user right management</li>
            <li>multiple users per database</li>
        </ul>
    </ul>
    <h3>Third party software</h3>
    <p>This software uses:
    <ul>
        <li><a href="https://jquery.com" target="blank">jQuery</a></li>
    </ul>
